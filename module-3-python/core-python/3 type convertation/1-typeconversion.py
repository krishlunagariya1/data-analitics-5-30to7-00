age = "45"
print(type(age))  # this will print <class 'str'>, indicating that age is currently a string
print(int(age))  # this will convert the string "45" to the integer 45
print(int(age) + 5)  # this will convert the string "45" to the integer 45 and then add 5 to it, resulting in 50


