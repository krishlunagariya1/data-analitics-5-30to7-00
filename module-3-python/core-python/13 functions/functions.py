def greet(name):
    print("good morning", name)
    print("How are you?")
    print("Thank you!")

greet() # Output: good morning How are you? Thank you!
greet("Krish") # Output: good morning Krish How are you? Thank you!
greet("Krish", "Rohit") # Output: good morning Krish How are you? Thank you!

