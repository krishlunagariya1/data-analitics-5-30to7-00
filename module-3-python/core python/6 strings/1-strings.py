name = "krish"

print(name[0])
print(name[1])
print(name[2])
print(name[3])
print(name[4])
print(name[-1])
print(name[-2])
print(name[0:3])  # this will print the characters from index 0 to index 2 (3 is not included)
print(name[1:])  # this will print the characters from index 1 to the end of the string
print(name[:4])  # this will print the characters from the beginning of the string to
print(name[-4:])  # this will print the characters from index -4 to the end of the string
print(name[::2])  # this will print every second character of the string, starting from index 0
print(name[1::2])  # this will print every second character of the string, starting from index 1
print(name[::-1])  # this will print the string in reverse order

